largura = float(input("Digite a largura do retângulo: "))
altura = float(input("Digite a altura do retângulo: "))

area = largura * altura
perimetro = 2 * (largura + altura)

print(f"Área do retângulo: {area:.2f}")
print(f"Perímetro do retângulo: {perimetro:.2f}")
