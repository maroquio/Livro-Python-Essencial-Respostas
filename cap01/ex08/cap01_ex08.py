import math

num = float(input("Digite um número: "))

seno = math.sin(num)
cosseno = math.cos(num)
tangente = math.tan(num)

print(f"Seno: {seno:.2f}")
print(f"Cosseno: {cosseno:.2f}")
print(f"Tangente: {tangente:.2f}")
